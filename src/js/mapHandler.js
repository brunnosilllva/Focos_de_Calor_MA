    redimensionar() {
        if (this.mapa) {
            this.mapa.invalidateSize();
        }
    }

    obterBounds() {
        return this.mapa ? this.mapa.getBounds() : null;
    }

    obterZoom() {
        return this.mapa ? this.mapa.getZoom() : null;
    }

    obterCentro() {
        return this.mapa ? this.mapa.getCenter() : null;
    }

    destruir() {
        if (this.mapa) {
            this.mapa.remove();
            this.mapa = null;
        }
        this.markers = [];
        this.clusters = null;
    }

